package com.example.sherly.medigent.model.user;

public class UserModel {
    String _id, nama_lengkap, email, password, telepon, jk, tgl_lahir, foto;

    public String get_id() {
        return _id;
    }

    public String getNama_lengkap() {
        return nama_lengkap;
    }

    public String getEmail() {
        return email;
    }

    public String getPassword() {
        return password;
    }

    public String getTelepon() {
        return telepon;
    }

    public String getJk() {
        return jk;
    }

    public String getTgl_lahir() {
        return tgl_lahir;
    }

    public String getFoto() {
        return foto;
    }
}
